#arreglos o list en python
materias = ['matematica', 'fisica' , ' quimica']
print (materias)
#ordenar elementos de la list
materias.sort()
print (materias[0]) #para imprimir la posicion en especifico
print (materias)

#obtener datos dentro de un arreglos
aprendiendo =f'estamos de el mes de {materias[0]}'
print(aprendiendo)

#reemplazar el valor de un arreglo
materias[2]= 'ingles'
print (materias)

#agregar un valor a un arreglo
materias.append('programacion')
print (materias)

#eliminar un valor de un arreglo

del materias[2]
print (materias)